GTHEX: EQU 030EH
OUTPUT: EQU 0389H
HXDSP: EQU 034FH
RDKBD: EQU 03BAH
CLEAR: EQU 02BEH

org 9000h 

MVI A,8BH
MVI B,01H
OUT 43H
START:
	IN 41H
	CPI 01H
	JZ CONT
	JMP START
CONT:
	MOV A,B
	OUT 40H
	MOV A,B
	RLC
	MOV B,A
	IN 41H
	ANI 03H
	CPI 03H
	JZ START
	CALL DELAY
	JMP CONT
DELAY:
	MVI C,03H
OUTLOOP:
	LXI D,0AF00H
INLOOP:
	DCX D
	MOV A,D
	ORA E
	JNZ INLOOP
	DCR C
	JNZ OUTLOOP
	RET
